function dedupeByPlaylistId(list) {
  const seen = new Set();
  return list.filter((p) => {
    if (seen.has(p.playlistId)) return false;
    seen.add(p.playlistId);
    return true;
  });
}

function mergeLists(base, extra) {
  return dedupeByPlaylistId([...(extra || []), ...(base || [])]);
}

function buildSubjectPlaylistsForExam(examId) {
  const extras = catalog.examExtras[examId] || {};
  const result = {};

  for (const subject of ["maths", "physics", "chemistry"]) {
    const base = catalog[subject];
    const ex = extras[subject] || {};
    result[subject] = {
      hindi: mergeLists(base.hindi, ex.hindi),
      english: mergeLists(base.english, ex.english),
      mostWatched: mergeLists(base.mostWatched, ex.mostWatched)
    };
  }
  return result;
}

